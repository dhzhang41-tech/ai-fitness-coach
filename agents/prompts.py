def determine_training_level(profile: dict, recent_logs: list) -> str:
    """
    基于 RP Strength 体系判断用户训练阶段。
    总分 0-2 -> novice, 3-4 -> intermediate, 5-7 -> advanced。
    保守优先，有疑问往低判。
    """
    score = 0

    weight = profile.get("weight_kg", 70)
    bench = profile.get("bench_1rm", 0)
    squat = profile.get("squat_1rm", 0)
    deadlift = profile.get("deadlift_1rm", 0)

    # 力量比值评分 (0-3分) —— 辅助指标
    if weight and weight > 0:
        if bench / weight >= 1.0:
            score += 1
        if squat / weight >= 1.25:
            score += 1
        if deadlift / weight >= 1.5:
            score += 1

    # 训练一致性评分 (0-2分)
    training_count = len(recent_logs)
    if training_count >= 6:
        score += 2
    elif training_count >= 3:
        score += 1

    if training_count > 0:
        avg_completion = (
            sum(l.get("completion_rate", 0) for l in recent_logs)
            / training_count
        )
        if avg_completion < 0.7:
            score = max(0, score - 1)

    # 恢复能力评分 (0-2分)
    sleep_scores = [
        l.get("sleep_score") for l in recent_logs
        if l.get("sleep_score") is not None
    ]
    stress_scores = [
        l.get("stress_score") for l in recent_logs
        if l.get("stress_score") is not None
    ]

    if sleep_scores:
        avg_sleep = sum(sleep_scores) / len(sleep_scores)
        if avg_sleep >= 8:
            score += 2
        elif avg_sleep >= 7:
            score += 1
    else:
        score += 1  # 无历史数据，保守默认 1 分

    if stress_scores:
        avg_stress = sum(stress_scores) / len(stress_scores)
        if avg_stress > 7:
            score = max(0, score - 1)

    # 综合判断
    if score <= 2:
        return "novice"
    elif score <= 4:
        return "intermediate"
    else:
        return "advanced"
